    private void Login_regActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Login_regActionPerformed
        // TODO add your handling code here:
        String username = Login_username.getText();
        String password = Login_password.getText();
        try {
            if (Actions.exists(username)) {
                //error msg exists
                Login_dialog.setText("User already registered!");
            } else if (!(SymbolEx(username, password))) {
                Login_dialog.setText("Invalid symbols! Or credentials too short!");
            } else {
                Actions.register(username, password);
                Login_dialog.setText("User succesfully registered!");
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(PasswordStorageUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(PasswordStorageUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Login_regActionPerformed

