    private void Login_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Login_ButtonActionPerformed
        // TODO add your handling code here:
        String username = Login_username.getText();
        String password = Login_password.getText();
        try {
            //Actions.login(username, password);
            if (Actions.login(username, password)) {
                Menu_username.setText(username);
                Login_window.setVisible(false);
                Menu_window.setVisible(true);
            } else {
                Login_dialog.setText("Invalid information!");
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(PasswordStorageUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Login_ButtonActionPerformed

